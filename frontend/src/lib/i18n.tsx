const translations = {
  en: enTranslations,
  zh: zhTranslations
} as const;
const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = window.localStorage.getItem("sim-language");
    return saved === "zh" ? "zh" : "en";
  });

  useEffect(() => {
    window.localStorage.setItem("sim-language", language);
  }, [language]);

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage,
    t: (key, params) => {
      const message = (translations[language] as Record<string, string>)[key] ?? key;
      if (!params) return message;
      return Object.entries(params).reduce((acc, [paramKey, value]) => acc.split(`{{${paramKey}}}`).join(String(value)), message);
    }
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}



